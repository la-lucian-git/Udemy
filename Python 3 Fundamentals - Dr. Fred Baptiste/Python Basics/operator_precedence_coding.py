print(100 - 20 + 50)  # Order does not matter
print((100 - 20) + 50)
print((100 + 50) - 20)

print(10 * 4 + 5 * 5)  # Multiplication first

print(10 * 2 ** 3)  # Power first

print(-4 ** 0.5)  # Incorrect
print((-4) ** 0.5)  # Square root

n = 10
print(n * n - 1)  # Incorrect
print(n * (n - 1))  # Incorrect
